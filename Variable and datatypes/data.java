public class data {
    public static void main(String[] args) {
        // bytes,short,int,long
        //bytes 8 bits from -128 to 127
        byte age=19;
        //short 16 bits from -32,768 to 32,767
        short year=2025;

        //int 32 bits from -2,147,483,648 to 2,147,483,647
        int population=1000000;
        //long 64 bits from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        long distanceToMoon=384400000L; // L is used to denote long literal

        // float and double
        // float 32 bits, single precision
        float pi=3.14f; // f is used to denote float literal
        // double 64 bits, double precision
        double e=2.718281828459045; // no suffix needed for double
    }
}
